class Calculator {
  double? number1;
  double? number2;
  Calculator(
    this.number1,
    this.number2,
  );

  double add(double number1, number2) {
    return number1 + number2;
  }

  double sub(double number1, number2) {
    return number1 - number2;
  }

  double mul(double number1, number2) {
    return number1 * number2;
  }

  double div(double number1, number2) {
    return number1 / number2;
  }
}
